<template>
  <div>
    <ul id = "list">
			<li v-for="item of list" :key="item.id" :id="item.id" draggable="true" @dragstart="drag($event)">{{item.content}}</li>
		</ul>
    <div class="panel-body points demo flow_chart" id="points" @drop="drop($event)" @dragover="allowDrop($event)">
    </div>
  </div>
 
</template>

<script>
import $ from 'jquery';
import { jsPlumb } from 'jsplumb';

require('../assets/css/demo.css');
require('../assets/css/jsplumb.css');

export default {
  name: 'Index',
  data() {
    return {
      list: [
				{id:'drag1', content:'元素1'},
				{id:'drag2', content:'元素2'},
				{id:'drag3', content:'元素3'},
				{id:'drag4', content:'元素4'}
			],
      data: {
        point: [
          {
            _id: '58c21d713819d56d68763918',
            name: 'MoeLove-Other',
            status: '0',
          },
          {
            _id: '58c21d803819d56d68763919',
            name: 'Moe',
            status: '1',
          },
          {
            _id: '58c21da83819d56d6876391a',
            name: 'Love',
            status: '0',
          },
          {
            _id: '58c63ecf3819d5a22f2c7f24',
            name: 'TaoBeier',
            status: '1',
          },
        ],
        location: [
          ['Moe', 4, 14],
          ['Love', 4, 24],
          ['TaoBeier', 4, 34],
          ['TaoBeier', 20, 24],
          ['MoeLove', 4, 4],
          ['drag1', 30, 30],
          ['drag2', 40, 40],
          ['drag3', 50, 100],
          ['drag4', 800, 30],
        ],
        line: [
          ['58c21d713819d56d68763918', '58c21d803819d56d68763919'],
          ['58c21d803819d56d68763919', '58c21da83819d56d6876391a'],
          ['58c21d803819d56d68763919', '58c63ecf3819d5a22f2c7f24'],
          ['58c21da83819d56d6876391a', '58c63ecf3819d5a22f2c7f24'],
        ],
      },
    };
  },
  methods: {
    createFlow(flowData) {
      console.log('Other created');
      const color = '#acd';
      const instance = jsPlumb.getInstance({
        // notice the 'curviness' argument to this Bezier curve.
        // the curves on this page are far smoother
        // than the curves on the first demo, which use the default curviness value.
        Connector: ['Bezier', { curviness: 50 }],
        Endpoint: ['Dot', { radius: 5 }],
        DragOptions: { cursor: 'pointer', zIndex: 5000 },
        PaintStyle: { lineWidth: 5, stroke: '#445566' },
        EndpointStyle: { radius: 9, fill: color, stroke: 'red' },
        HoverPaintStyle: { stroke: '#ec9f2e', lineWidth: 4 },
        EndpointHoverStyle: { fill: '#ec9f2e', stroke: '#acd' },
        ConnectionOverlays: [
          ['Arrow', {
            location: 1,
            id: 'arrow',
            length: 4,
            foldback: 0.8,
            paintStyle: {
              lineWidth: 5,
              stroke: 'lightgray',
              fill: 'lightgray',
            },
          }],
        ],
        Container: 'points',
      });

      // suspend drawing and initialise.
      instance.batch(() => {
        // declare some common values:
        const arrowCommon = { foldback: 0.7, width: 12 };
        // use three-arg spec to create two different arrows with the common values:
        const overlays = [
          ['Arrow', { location: 0.7 }, arrowCommon],
          ['Label', { label: 'custom label', id: 'label' }],
        ];
        // init point
        for (const point of flowData.point) {
          $('.points').append(
            `<div id="${point._id}" class="point chart_act_${point.status} ${point.name}">${point.name}</div>`,
          );

          instance.addEndpoint(point._id, {
            uuid: `${point._id}-bottom`,
            anchor: 'Bottom',
            maxConnections: -1,
            // connectorStyle: { stroke: 'green' },
          }, {
            isSource: true,
            isTarget: true,
            dragAllowedWhenFull: true,
          });
          instance.addEndpoint(point._id, {
            uuid: `${point._id}-top`,
            anchor: 'Top',
            maxConnections: -1,
            // connectorStyle: { stroke: 'gray' },
          }, {
            isSource: true,
            isTarget: true,
            dragAllowedWhenFull: true,
          });
        }

        // init transition
        for (const i of flowData.line) {
          const uuid = [`${i[0]}-bottom`, `${i[1]}-top`];
          instance.connect({
            uuids: uuid,
            overlays,
          });
        }

        // init location
        for (const i of flowData.location) {
          $(`.${i[0]}`).css('left', i[1] * 20);
          $(`.${i[0]}`).css('top', i[2] * 20);
        }

        for (const point of flowData.point) {
          instance.draggable(`${point._id}`);
        }
      });

      jsPlumb.fire('jsPlumbDemoLoaded', instance);
    },
    allowDrop (ev) {
			// document.getElementById(id).style.left = ev.offsetX + "px";
      // document.getElementById(id).style.top = ev.offsetY + "px";
			ev.preventDefault();
		},
    drag (ev) {
			ev.dataTransfer.setData("Text",ev.target.id);
			ev.dataTransfer.setData("Title",ev.target.innerHTML);
		},
		drop (ev) {
			let id = ev.dataTransfer.getData("Text");
      let data = ev.dataTransfer.getData("Title");
      
			this.data.point.push({
				_id:id, 
        name:data,
        status: '0',
      })
			this.list = this.list.filter(l => l.id !== id)
      // ev.target.appendChild(document.getElementById(data));
      console.log(this.data)
      this.createFlow(this.data);
			ev.preventDefault();
		}
  },
  mounted() {
    jsPlumb.ready(() => {
      this.createFlow(this.data);
    });
  },
};
</script>

